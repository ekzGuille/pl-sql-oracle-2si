CREATE OR REPLACE PROCEDURE CAMBIAR_LOC(ID NUMBER, NUEVA_LOC VARCHAR2)
AS 
BEGIN
  UPDATE DEPART
    SET DEPART.LOC = NUEVA_LOC
    WHERE DEPART.DEPT_NO = ID;

EXCEPTION 
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('NO SE ECUENTRA EL DEPARTAMENTO');

END CAMBIAR_LOC;
/


