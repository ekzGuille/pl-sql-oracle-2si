CREATE OR REPLACE PROCEDURE CALCULAR_TRIENIOS(FECHA1 DATE, FECHA2 DATE)
AS
  V_TRIENIOS NUMBER;
BEGIN 
  V_TRIENIOS := CALCULAR_ANIOS(FECHA1, FECHA2);
  IF V_TRIENIOS >= 3 THEN
    DBMS_OUTPUT.PUT_LINE('HAY ' || TRUNC(V_TRIENIOS/3) ||' TRIENIOS ENTRE LAS DOS FECHAS');
  ELSE
    DBMS_OUTPUT.PUT_LINE('HAY MENOS DE 3 ANIOS ENTRE LAS FECHAS ESTABLECIDAS');
  END IF;
END CALCULAR_TRIENIOS;
/


