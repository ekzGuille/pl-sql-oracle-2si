CREATE OR REPLACE FUNCTION CALCULAR_ANIOS(FECHA1 DATE, FECHA2 DATE) RETURN NUMBER
AS 
  V_ANIOS NUMBER;
BEGIN
  IF FECHA1 >= FECHA2 THEN
    V_ANIOS := FECHA1 - FECHA2;
  ELSE
    V_ANIOS := FECHA2 - FECHA1;
  END IF;
  RETURN TRUNC(V_ANIOS/365);
END CALCULAR_ANIOS;
/

-- Obtener el resultado de la funcion
BEGIN
  DBMS_OUTPUT.PUT_LINE('HAN PASADO ' || CALCULAR_ANIOS('02/02/1999','22/07/1998') || ' ANIOS.');
END;
/ 

