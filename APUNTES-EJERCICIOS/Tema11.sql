CREATE OR REPLACE PROCEDURE INVIERTE(FRASE VARCHAR2)
AS
  V_FRASE VARCHAR2(32767) DEFAULT '';
  V_CONTADOR NUMBER;
BEGIN
  FOR V_CONTADOR IN REVERSE 1..LENGTH(FRASE)
  LOOP
    V_FRASE := CONCAT(V_FRASE, SUBSTR(FRASE,V_CONTADOR,1));
  END LOOP;
DBMS_OUTPUT.PUT_LINE('EL RESULTAOD ES: ' || V_FRASE);
END INVIERTE;
/


