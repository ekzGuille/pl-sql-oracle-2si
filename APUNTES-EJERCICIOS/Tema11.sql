CREATE OR REPLACE PROCEDURE BORRAR_EMPLE(ID NUMBER)
AS
BEGIN
  DELETE 
    FROM EMPLE
    WHERE EMPLE.EMP_NO = ID;
  DBMS_OUTPUT.PUT_LINE('EMPLEADO ' || ID || ' BORRADO CON EXITO');

EXCEPTION
  WHEN NO_DATA_FOUND THEN 
    DBMS_OUTPUT.PUT_LINE('NO SE ENCUENTRA EL EMPLEADO');

END BORRAR_EMPLE;
/


